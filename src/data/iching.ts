// Generate hexagram from coin tosses
export function generateHexagram(seed?: string): { hexagram: typeof HEXAGRAMS[0]; lines: number[] } {
  const lines: number[] = [];
  
  // Simple PRNG
  let hash = seed ? hashString(seed) : Date.now();
  
  for (let i = 0; i < 6; i++) {
    // 3 coins = 1 line
    // 3 heads = 9 (old yang)
    // 2 heads + 1 tail = 7 (young yang)
    // 1 head + 2 tails = 8 (young yin)
    // 3 tails = 6 (old yin)
    const tosses = [(hash = (hash * 1103515245 + 12345) % 2^31) % 2];
    hash = (hash * 1103515245 + 12345) % 2^31;
    tosses.push(hash % 2);
    hash = (hash * 1103515245 + 12345) % 2^31;
    tosses.push(hash % 2);
    
    const sum = tosses.reduce((a, b) => a + b, 0);
    // Simplified: just use random
    lines.push(Math.random() > 0.5 ? 1 : 0);
  }
  
  // Find matching hexagram
  const hexagram = HEXAGRAMS.find(h => 
    h.lines.every((l, i) => l === lines[i])
  ) || HEXAGRAMS[0];
  
  return { hexagram, lines };
}

function hashString(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = ((hash << 5) - hash) + str.charCodeAt(i);
    hash |= 0;
  }
  return Math.abs(hash);
}
