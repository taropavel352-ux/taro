export function getDailyHoroscope(sign: string, dateStr: string) {
  const date = new Date(dateStr);
  const dayOfWeek = date.getDay();
  
  // Простой способ получить "случайный" гороскоп на основе даты
  const dayOfYear = getDayOfYear(date);
  const index = (dayOfYear + sign.charCodeAt(0)) % generalHoroscopes[sign as keyof typeof generalHoroscopes].length;
  
  // Определяем фазу луны
  const moonPhase = getMoonPhase(date);
  
  return {
    general: generalHoroscopes[sign as keyof typeof generalHoroscopes][index],
    luck: moonPhaseLuck[moonPhase as keyof typeof moonPhaseLuck],
    dayTip: dayOfWeekLuck[dayOfWeek],
    lucky_number: (dayOfYear % 9) + 1,
    lucky_color: getLuckyColor(dayOfYear),
    compatible_sign: getCompatibleSign(sign, dayOfYear)
  };
}

function getDayOfYear(date: Date): number {
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - start.getTime();
  return Math.floor(diff / (1000 * 60 * 60 * 24));
}

function getMoonPhase(date: Date): string {
  const knownNewMoon = new Date(2000, 0, 6, 18, 14);
  const lunarCycle = 29.53058867;
  const diffMs = date.getTime() - knownNewMoon.getTime();
  const diffDays = diffMs / (1000 * 60 * 60 * 24);
  const lunarAge = diffDays % lunarCycle;
  
  if (lunarAge < 1.85) return 'new';
  if (lunarAge < 7.38) return 'waxing';
  if (lunarAge < 14.77) return 'full';
  return 'waning';
}

